void printMsg(char *format, ...) {
	char str[80];

	/*Extract the the argument list using VA apis */
	va_list args;
	va_start(args, format);
	vsprintf(str, format, args);
	HAL_UART_Transmit(&huart2, (uint8_t*) str, strlen(str), HAL_MAX_DELAY);
	va_end(args);
}

uint8_t i2cScan(void) {
	char info[] = "Scanning I2C bus...\r\n";
	printMsg(info);

	HAL_StatusTypeDef res;

	for (uint16_t i = 0; i < 128; i++) {
		res = HAL_I2C_IsDeviceReady(&hi2c1, i << 1, 1, 10);
		if (res == HAL_OK && (i) == 0x3D) {
			printMsg("OLED found at 0x%02X\n", i);
		} else if (res == HAL_OK && (i) == 0x2F) {
			printMsg("MCP4018 found at 0x%02X\n", i);
		} else if (res == HAL_OK) {
			printMsg("Unknown device found at 0x%02X\n", i);
		}
	}
	printMsg("returning from scan\n");
	return 0;
}

uint8_t MCP4018_ReadVal() {
	uint8_t retVal = 0;
	HAL_StatusTypeDef err = HAL_I2C_Master_Receive(&hi2c1, 0x2F << 1, &retVal, 1, HAL_MAX_DELAY);
	if(err == HAL_BUSY)
		printMsg("BUSY\n");
	else if(err == HAL_ERROR)
		printMsg("NACK\n");
	return retVal;
}

HAL_StatusTypeDef MCP4018_WriteVal(uint8_t val) {
	return HAL_I2C_Master_Transmit(&hi2c1, 0x2F << 1, &val, 1, HAL_MAX_DELAY);
}
